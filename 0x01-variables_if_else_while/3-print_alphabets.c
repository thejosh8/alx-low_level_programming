#include<stdio.h>
/**
* main - Entry point
* Return: 0 If successful
*
* Description: Bla Bla Bla
*/
int main(void)
{
int capital;
int lower;
for (lower = 'a'; lower <= 'z'; lower++)
{
	putchar(lower);
}
for (capital = 'A'; capital <= 'Z'; capital++)
{
putchar(capital);
}
printf("\n");
return (0);
}
